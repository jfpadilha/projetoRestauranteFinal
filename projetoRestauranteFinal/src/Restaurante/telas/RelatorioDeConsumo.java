    private void setarValores()
    {
        ControladorMesasFechadas ctm = new ControladorMesasFechadas();
        
        Double total = ctm.consultarValorTotalConsumido();
        String totalS = total.toString();
        
        Double media = ctm.consultarMediaDeConsumo();
        String mediaS = media.toString();
        
        tfTotal.setText(totalS);
        tfMedia.setText(mediaS);
        tfTotal.setEditable(false);
        tfMedia.setEditable(false);
    }

